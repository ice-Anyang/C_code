//3.在屏幕上打印杨辉三角。
//1
//1 1
//1 2 1
//1 3 3 1
int main()
{
	int s = 1;
	int h = 0;               // 数值和高度
	int i = 0;
	int j = 0;                        // 循环计数
	scanf("%d", &h);                 // 输入层数
	printf("1\n");                   // 输出第一个 1
	for (i = 2; i <= h;s = 1,i++)         // 行数 i 从 2 到层高
	{
		
		printf("1 ");                // 第一个 1
		for (j = 1; j <= i - 2; j++) 
		{
			printf("%d ", (s = (i - j) * s / j));
		}
		printf("1\n");               // 最后一个 1，换行
	}
	system("pause");                       
	return 0;
}